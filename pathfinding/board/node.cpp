#include "node.h"

Node::Node()
{
	//parent = 0;
	i = 0;
	f = 0;
	g = 0;
	h = 0;
}

// ------------------------------------------------------------------------------


Node::Node(int f_, int g_, int h_, int i_)
{
	//parent = 0;
	i = i_;
	f = f_;
	g = g_;
	h = h_;
}

// ------------------------------------------------------------------------------


void Node::init(int if_)
{
	i = if_;
	f = if_;
	g = 0;
	h = 0;
}

// ------------------------------------------------------------------------------


void Node::setI(int i_)
{
	i = i_;
}

// ------------------------------------------------------------------------------


void Node::setFGH(int g_, int h_)
{
	f = g_ + h_;
	g = g_;
	h = h_;

	i = g_;
}

// ------------------------------------------------------------------------------


int Node::getI()
{
	return i;
}

// ------------------------------------------------------------------------------


int Node::getF()
{
	return f;
}

